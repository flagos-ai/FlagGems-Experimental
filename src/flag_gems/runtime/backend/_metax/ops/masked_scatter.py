@libentry()
@triton.jit
def masked_scatter_single_pass_kernel(
    inp_ptr, mask_ptr, src_ptr, N, BLOCK_SIZE: tl.constexpr
):
    pid = tl.program_id(0)
    offsets = pid * BLOCK_SIZE + tl.arange(0, BLOCK_SIZE)

    block_mask = offsets < N

    mask_val = tl.load(mask_ptr + offsets, mask=block_mask, other=0).to(tl.int1)

    mask_ints = mask_val.to(tl.int32)
    src_indices = tl.cumsum(mask_ints, axis=0) - 1

    active = block_mask & mask_val
    src_val = tl.load(src_ptr + src_indices, mask=active)
    tl.store(inp_ptr + offsets, src_val, mask=active)


@libentry()
@triton.jit(do_not_specialize=["N", "num_blocks", "num_blocks_per_row"])
def mask_block_sum_kernel(
    mask_ptr,
    block_sums_ptr,
    N,
    num_blocks,
    num_blocks_per_row,
    BLOCK_SIZE: tl.constexpr,
):
    """Compute per-row mask sums without inter-block synchronization.

    Each program scans its assigned mask blocks and accumulates the
    count of True values.  The result is stored in block_sums_ptr[row_id].
    No atomic-add barrier is needed because the host computes the prefix
    sums with torch.cumsum afterwards.
    """
    row_id = tl.program_id(0)
    start_block = row_id * num_blocks_per_row
    offset = start_block * BLOCK_SIZE + tl.arange(0, BLOCK_SIZE)
    acc = tl.zeros((BLOCK_SIZE,), dtype=block_sums_ptr.dtype.element_ty)

    last_block_id = min(num_blocks - 1, start_block + num_blocks_per_row - 1)

    for _ in range(start_block, last_block_id):
        select = tl.load(mask_ptr + offset)
        select_ints = select.to(block_sums_ptr.dtype.element_ty)
        acc += select_ints
        offset += BLOCK_SIZE

    select = tl.load(mask_ptr + offset, mask=offset < N, other=0)
    select_ints = select.to(block_sums_ptr.dtype.element_ty)
    acc += select_ints

    part_sum = tl.sum(acc, axis=0)
    tl.store(block_sums_ptr + row_id, part_sum)


@libentry()
@triton.jit(do_not_specialize=["N", "num_blocks", "num_blocks_per_row"])
def masked_scatter_kernel(
    inp_ptr,
    mask_ptr,
    src_ptr,
    part_sums_ptr,
    N,
    num_blocks,
    num_blocks_per_row,
    BLOCK_SIZE: tl.constexpr,
):
    row_id = tl.program_id(0)

    start_block = row_id * num_blocks_per_row
    offset = start_block * BLOCK_SIZE + tl.arange(0, BLOCK_SIZE)

    advance = tl.load(part_sums_ptr + row_id)

    last_block_id = min(num_blocks - 1, start_block + num_blocks_per_row - 1)

    for _ in range(start_block, last_block_id):
        select_mask = tl.load(mask_ptr + offset).to(tl.int1)
        select_ints = select_mask.to(tl.int32)

        block_cumsum = tl.cumsum(select_ints, axis=0) - 1
        global_src_idx = advance + block_cumsum

        advance += tl.sum(select_ints, axis=0)

        src_val = tl.load(src_ptr + global_src_idx, mask=select_mask)
        tl.store(inp_ptr + offset, src_val, mask=select_mask)

        offset += BLOCK_SIZE

    block_mask = offset < N
    select_mask = tl.load(mask_ptr + offset, mask=block_mask, other=0).to(tl.int1)

    select_ints = select_mask.to(tl.int32)
    block_cumsum = tl.cumsum(select_ints, axis=0) - 1
    global_src_idx = advance + block_cumsum

    active = block_mask & select_mask
    src_val = tl.load(src_ptr + global_src_idx, mask=active)
    tl.store(inp_ptr + offset, src_val, mask=active)


def _get_max_warps(device):
    """Return the maximum number of warps per block for the given device.

    Different backends have different thread-per-block limits.  Metax
    (warp_size=64) supports at most 512 threads/block (8 warps).  CUDA
    (warp_size=32) supports 1024 threads/block but we cap at 16 warps
    for this kernel to avoid over-subscription.
    """
    warp_size = torch.cuda.get_device_properties(device).warp_size
    return 512 // warp_size  # 8 for Metax, 16 for CUDA


def masked_scatter_impl(inp, mask, source, N):
    max_warps = _get_max_warps(inp.device)

    if N <= 4096:
        BLOCK_SIZE = triton.next_power_of_2(N)
        num_warps = 4
        if BLOCK_SIZE >= 2048:
            num_warps = min(8, max_warps)
        if BLOCK_SIZE >= 4096:
            num_warps = min(16, max_warps)

        masked_scatter_single_pass_kernel[(1,)](
            inp, mask, source, N, BLOCK_SIZE=BLOCK_SIZE, num_warps=num_warps
        )
        return inp

    BLOCK_SIZE = bracket_next_power_of_2(N, 128, 4096)
    warp_size = torch.cuda.get_device_properties(inp.device).warp_size
    num_warps = min(max_warps, BLOCK_SIZE // warp_size)

    mp_count = torch_device_fn.get_device_properties(inp.device).multi_processor_count
    n_blocks = triton.cdiv(N, BLOCK_SIZE)
    np = min(n_blocks, mp_count)
    n_blocks_per_row = triton.cdiv(n_blocks, np)
    np = triton.cdiv(n_blocks, n_blocks_per_row)

    with torch_device_fn.device(inp.device):
        dtype = torch.int32 if N < 2**31 else torch.int64
        block_sums = torch.empty(np, dtype=dtype, device=inp.device)

        # Step 1: Compute per-row mask sums (fully parallel, no atomics).
        mask_block_sum_kernel[(np,)](
            mask,
            block_sums,
            N,
            n_blocks,
            n_blocks_per_row,
            BLOCK_SIZE=BLOCK_SIZE,
            num_warps=num_warps,
        )

        # Step 2: Compute exclusive prefix sums via torch.cumsum
        # (fast GPU library call, no kernel barrier needed).
        prefix_sums = torch.zeros(np + 1, dtype=dtype, device=inp.device)
        torch.cumsum(block_sums, dim=0, out=prefix_sums[1:])

        # Step 3: Scatter using pre-computed prefix sums.
        masked_scatter_kernel[(np,)](
            inp,
            mask,
            source,
            prefix_sums,
            N,
            n_blocks,
            n_blocks_per_row,
            BLOCK_SIZE=BLOCK_SIZE,
            num_warps=num_warps,
        )

    return inp


