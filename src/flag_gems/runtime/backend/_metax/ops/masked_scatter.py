"""Metax-specific implementation of masked_scatter and masked_scatter_.

The generic implementation uses a two-pass algorithm with an atomic-add
barrier in mask_part_sum_kernel, where the last arriving block computes
prefix sums while other SMs sit idle.  This causes severe serialization
on Metax GPUs.

This override replaces the barrier with separate kernel launches plus a
torch.cumsum call on a small auxiliary array (~np elements), eliminating
inter-block synchronization.  num_warps is also made device-aware to
respect Metax's 512-thread-per-block limit (warp_size=64 -> max 8 warps).
"""

