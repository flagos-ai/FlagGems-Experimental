"""Metax-specific implementation of upsample_linear1d.

The generic implementation uses a 2D launch grid with a hardcoded BLOCK_SIZE
of 256 and no autotuning.  This override switches to a 1D grid over all
output elements and autotunes BLOCK_SIZE and num_warps, which allows the
Metax warp scheduler (warp_size=64) to be utilised effectively and reduces
the number of undersized blocks on large tensors.
"""

import logging
import math

import torch
import triton
import triton.language as tl

from flag_gems import runtime
from flag_gems.runtime import device, torch_device_fn

logger = logging.getLogger(__name__)
device = device.name


@triton.autotune(
    configs=runtime.get_tuned_config("upsample_linear1d"),
    key=["N", "C", "W_in", "W_out"],
)
@triton.heuristics(
    values={
        "BLOCK_SIZE": lambda args: 1024,
        "num_warps": lambda args: 8,
    },
)
@triton.jit
def upsample_linear1d_metax_kernel(
    input_ptr,
    output_ptr,
    NC,
    W_in,
    W_out,
    scale,
    bias,
    BLOCK_SIZE: tl.constexpr,
):
    pid = tl.program_id(0)
    total_elements = NC * W_out
    offs = pid * BLOCK_SIZE + tl.arange(0, BLOCK_SIZE)
    mask = offs < total_elements

    offs_w = offs % W_out
    offs_nc = offs // W_out
    base_in = offs_nc * W_in

    offs_w_f = offs_w.to(tl.float32)
    src = offs_w_f * scale + bias
    src = tl.maximum(0.0, tl.minimum(src, W_in - 1.0))

    lower = tl.floor(src).to(tl.int32)
    upper = tl.minimum(lower + 1, W_in - 1)
    t = src - lower.to(tl.float32)

    x0 = tl.load(input_ptr + base_in + lower, mask=mask)
    x1 = tl.load(input_ptr + base_in + upper, mask=mask)

    out = (1.0 - t) * x0.to(tl.float32) + t * x1.to(tl.float32)
    tl.store(output_ptr + offs, out.to(x0.dtype), mask=mask)


def upsample_linear1d(
    self: torch.Tensor,
    output_size,
    align_corners: bool,
    scales: float = None,
):
    logger.debug("GEMS_METAX UPSAMPLE_LINEAR1D")
    assert self.ndim == 3, "Input must be [N, C, W]"
    assert self.device.type == device

    N, C, W_in = self.shape
    NC = N * C

    if output_size is not None:
        W_out = int(
            output_size[0] if isinstance(output_size, (list, tuple)) else output_size
        )
    else:
        assert scales is not None
        W_out = int(math.floor(W_in * scales))

    # Align corners: compute scale and bias for coordinate mapping
    if align_corners:
        scale_val = (W_in - 1.0) / (W_out - 1.0) if W_out > 1 else 0.0
        bias_val = 0.0
    else:
        real_scale = (1.0 / scales) if scales is not None else (W_in / W_out)
        scale_val = real_scale
        bias_val = 0.5 * real_scale - 0.5

    inp = self.contiguous().view(NC, W_in)
    out = torch.empty((NC, W_out), device=self.device, dtype=self.dtype)

    with torch_device_fn.device(self.device):
        total_elements = NC * W_out
        grid = lambda meta: (triton.cdiv(total_elements, meta["BLOCK_SIZE"]),)
        upsample_linear1d_metax_kernel[grid](
            inp, out, NC, W_in, W_out, scale_val, bias_val
        )

    return out.view(N, C, W_out)
