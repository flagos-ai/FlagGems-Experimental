# Tile parameters — shared across tiers 2 and 3
# ---------------------------------------------------------------------------


def _select_tile_params(num_tasks):
    tile_size = max(_MIN_TILE_SIZE, triton.cdiv(num_tasks, _TARGET_TILES_MAX))
    tile_size = triton.next_power_of_2(tile_size)
    tile_size = min(tile_size, _MAX_TILE_SIZE)
    gcta = triton.cdiv(num_tasks, tile_size)
    while gcta > _TARGET_TILES_MAX and tile_size < _MAX_TILE_SIZE:
        tile_size = min(_MAX_TILE_SIZE, tile_size * 2)
        gcta = triton.cdiv(num_tasks, tile_size)
    npow = triton.next_power_of_2(gcta)
    cta = gcta
    tpc = triton.cdiv(num_tasks, tile_size * cta)
    nw = 8 if tpc == 1 else _MAX_NUM_WARPS
    return tile_size, gcta, npow, cta, tpc, nw


def _make_tile_prefix(tile_sum, out_size_ref):
    prefix = torch.cumsum(tile_sum, dim=0)
    out_size_ref[0] = prefix[-1].item()
    tile_prefix = torch.empty_like(prefix)
    tile_prefix[0] = 0
    tile_prefix[1:] = prefix[:-1]
    return tile_prefix


