def _medium_path(data, return_inverse, return_counts):
    num_tasks = data.numel()
    tile_size, gcta, npow, cta, tpc, nw = _select_tile_params(num_tasks)
    grid = (cta, 1, 1)

    ne_result = torch.empty(num_tasks, dtype=torch.bool, device=data.device)
    tile_sum = torch.empty(gcta, dtype=torch.int64, device=data.device)
    data_out = torch.empty_like(data)
    inv = (
        torch.empty(num_tasks, dtype=torch.int64, device=data.device)
        if return_inverse
        else None
    )
    idx = (
        torch.empty(num_tasks, dtype=torch.int64, device=data.device)
        if return_counts
        else None
    )

    with torch_device_fn.device(data.device.index):
        local_ne_consecutive_kernel[grid](
            data,
            ne_result,
            tile_sum,
            gcta,
            num_tasks,
            tiles_per_cta=tpc,
            tile_size=tile_size,
            num_warps=nw,
        )
        _generic_global_cumsum_kernel[grid](
            ne_result,
            tile_sum,
            data,
            data_out,
            inv,
            idx,
            cta,
            gcta,
            npow,
            num_tasks,
            tiles_per_cta=tpc,
            tile_size=tile_size,
            one_tile_per_cta=tpc == 1,
            return_inverse=return_inverse,
            return_counts=return_counts,
            num_warps=nw,
        )
        out_size = tile_sum[-1].item()

        counts = None
        if return_counts:
            idx = idx[:out_size]
            counts = torch.empty_like(idx)
            output_counts_kernel[grid](
                idx,
                num_tasks,
                counts,
                out_size,
                tpc,
                tile_size,
                num_warps=nw,
            )

