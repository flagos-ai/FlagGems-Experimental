def unique_consecutive(
    input: torch.Tensor,
    return_inverse: bool = False,
    return_counts: bool = False,
    dim: int = None,
):
    logger.debug("GEMS_ILUVATAR UNIQUE_CONSECUTIVE")
    if dim is not None:
        return torch.unique_consecutive(
            input,
            return_inverse=return_inverse,
            return_counts=return_counts,
            dim=dim,
        )

    flat = input.ravel()
    n = flat.numel()

    if n == 0:
        out = torch.empty(0, dtype=input.dtype, device=input.device)
        inv = (
            torch.empty(0, dtype=torch.int64, device=input.device)
            if return_inverse
            else None
        )
        cnt = (
            torch.empty(0, dtype=torch.int64, device=input.device)
            if return_counts
            else None
        )
        return out, inv, cnt

    if n <= _SIMPLE_PATH_THRESHOLD:
        out, inv, cnt = simple_unique_consecutive_flat(
            flat, return_inverse, return_counts
        )
    elif n <= _LARGE_PATH_THRESHOLD:
        out, inv, cnt = _medium_path(flat, return_inverse, return_counts)
    else:
        out, inv, cnt = _large_path(flat, return_inverse, return_counts)

    if inv is not None:
        inv = inv.view_as(input)
    return out, inv, cnt
