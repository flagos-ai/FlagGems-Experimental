@triton.jit
def _iluvatar_prefix_impl(
    global_pid,
    prefix_total,
    ne_result_ptr: tl.tensor,
    tile_prefix_ptr: tl.tensor,
    data_ptr: tl.tensor,
    data_out_ptr: tl.tensor,
    inverse_indices_ptr: tl.tensor,
    idx_ptr: tl.tensor,
    num_tasks: int,
    tile_size: tl.constexpr,
    return_inverse: tl.constexpr,
    return_counts: tl.constexpr,
):
    offset = global_pid * tile_size
    r = tl.arange(0, tile_size)
    i0 = offset + r
    mask = i0 < num_tasks
    data = tl.load(data_ptr + i0, mask=mask)
    pre = tl.load(tile_prefix_ptr + global_pid)
    ne = tl.load(ne_result_ptr + i0, mask=mask)
    cumsum = tl.cumsum(ne.to(tl.int32)) + pre
    out_idx = cumsum - 1
    w = ne.to(tl.int1) & mask
    tl.store(data_out_ptr + out_idx, data, mask=w)
    if return_inverse:
        tl.store(inverse_indices_ptr + i0, out_idx, mask=mask)
    if return_counts:
        tl.store(idx_ptr + out_idx, i0, mask=w)


