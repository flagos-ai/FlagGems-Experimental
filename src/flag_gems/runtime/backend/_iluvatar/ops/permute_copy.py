def _try_merge_3d_to_2d(shape, dims):
    """Try to reduce a 3D permute_copy to a 2D problem by merging consecutive
    input dims that appear consecutively in the output permutation.

    Returns ``(M, N, is_transpose, src_2d)`` where:
    - src_2d is the reshaped input of shape ``[M, N]``
    - ``is_transpose=True`` means the 2D problem is a transpose (output [N, M])
    - ``is_transpose=False`` means the 2D problem is identity (output [M, N])
    Returns ``None`` if no merge is possible.
    """
    if len(shape) != 3:
        return None

    for merge_dim in (0, 1):
        pos_a = dims.index(merge_dim)
        pos_b = dims.index(merge_dim + 1)
        if abs(pos_a - pos_b) != 1:
            continue

        if merge_dim == 0:
            # Merge input dims 0 and 1
            M, N = shape[0] * shape[1], shape[2]
            # input dim 2 maps to output position; merged dims at min(pos_a, pos_b)
            if dims.index(2) < min(pos_a, pos_b):
                is_transpose = True  # output [N, M] = [shape[2], shape[0]*shape[1]]
            else:
                is_transpose = False  # output [M, N] = [shape[0]*shape[1], shape[2]]
        else:
            # Merge input dims 1 and 2
            M, N = shape[0], shape[1] * shape[2]
            if dims.index(0) < min(pos_a, pos_b):
                is_transpose = False  # output [M, N]
            else:
                is_transpose = True  # output [N, M]

        return M, N, is_transpose

    return None


