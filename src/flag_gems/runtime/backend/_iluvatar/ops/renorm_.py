logger = logging.getLogger(__name__)

# Heuristic threshold: when per-slice reduction length N <= this value,
# a single tile covers the whole slice and the norm + rescale fuse into
# one load/store pass (INPUT_FITS=True).
_REDUCTION_FIT_CAP = 1024
# Target register tile size used to derive BLOCK_M from BLOCK_N.
_TILE_TARGET = 4096


