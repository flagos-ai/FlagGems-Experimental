def _block_heuristics(N):
    """Pick (BLOCK_M, BLOCK_N, INPUT_FITS, num_warps) for a reduction
    length N.  Iluvatar-tuned for num_stages=1."""
    if N <= _REDUCTION_FIT_CAP:
        input_fits = True
        block_n = min(triton.next_power_of_2(N), _REDUCTION_FIT_CAP)
    else:
        input_fits = False
        block_n = 256
    block_m = max(4, min(128, _TILE_TARGET // block_n))
    tile = block_n * block_m
    if tile <= 1024:
        num_warps = 1
    elif tile <= 2048:
        num_warps = 2
    elif tile <= 8192:
        num_warps = 4
    else:
        num_warps = 8
    return block_m, block_n, input_fits, num_warps


