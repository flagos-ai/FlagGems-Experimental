def renorm_(x, p, dim, maxnorm):
    """In-place renorm — Iluvatar optimized 2-D tiled kernel.

    Compared to the generic one-block-per-row implementation this version:
      - tiles BLOCK_M rows per program to amortize launch overhead
      - uses a 2-D (BLOCK_M × BLOCK_N) decomposition for balanced occupancy
      - operates on strided views of 2-D tensors to avoid permute/copy
      - short-circuits p=2.0 → x*x and p=1.0 → abs, skipping tl_extra_shim.pow
    """
    logger.debug("GEMS_ILUVATAR RENORM_")
    p = float(p)
    maxnorm = float(maxnorm)
    dim = dim % x.ndim

    if x.ndim == 2:
        # Fast path: operate in place on the original strided view —
        # no permute-contiguous copy, no copy-back, one launch.
        other = 1 - dim
        M = x.shape[dim]
        N = x.shape[other]
        sM = x.stride(dim)
        sN = x.stride(other)
        _launch_2d(x, M, N, sM, sN, p, maxnorm)
        return x

    # General path for rank > 2: bring ``dim`` to the front, materialize
    # a contiguous (M, N) buffer, rescale in place, then copy back.
    perm = [dim] + [i for i in range(x.ndim) if i != dim]
    inv_perm = [perm.index(i) for i in range(x.ndim)]
    x_perm = x.permute(perm).contiguous()
    M = x_perm.shape[0]
    N = x_perm[0].numel()
    _launch_2d(x_perm, M, N, N, 1, p, maxnorm)
    x.copy_(x_perm.permute(inv_perm))
    return x
