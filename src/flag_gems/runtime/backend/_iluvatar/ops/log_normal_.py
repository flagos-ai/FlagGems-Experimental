def fused_log_normal_kernel(
    out_ptr,
    N,
    mean,
    std,
    philox_seed,
    philox_offset,
    BLOCK_SIZE: tl.constexpr,
):
    """Fused log-normal sampler: Philox RNG -> uniform -> Box-Muller normal
    -> log-normal transform, all in a single kernel.

    Key optimizations vs the two-kernel generic implementation:
    - No temporary fp32 buffer (saves memory bandwidth on huge shapes).
    - Single kernel launch (halves launch overhead).
    - exp/log expressed via exp2/log2 with pre-multiplied constants
      (std*LOG2E, mean*LOG2E, -2*ln2) and fused-multiply-add for
      measurably faster evaluation than natural-base tl.exp/tl.log.
    """
    philox_seed = philox_seed.to(tl.int64)
    philox_offset = philox_offset.to(tl.int64)
    c0 = (philox_offset & 0xFFFFFFFF).to(tl.uint32)
    c1 = ((philox_offset >> 32) & 0xFFFFFFFF).to(tl.uint32)
    i4 = tl.program_id(0) * BLOCK_SIZE + tl.arange(0, BLOCK_SIZE)
    c0 += i4
    _O = c0 * 0
    r0, r1, r2, r3 = tl.philox(philox_seed, c0, c1, _O, _O)
    r0 = uint_to_uniform_float(r0)
    r1 = uint_to_uniform_float(r1)
    r2 = uint_to_uniform_float(r2)
    r3 = uint_to_uniform_float(r3)

    # Box-Muller: r = sqrt(-2 ln u), n = r * cos/sin(2 pi u2)
    u1 = tl.maximum(1.0e-7, r0)
    th = TWO_PI * r1
    r = tl.sqrt(-2.0 * tl.math.log2(u1) * LN2)
    s, c = fast_sin_cos(th)
    n0 = r * c
    n1 = r * s

    u2 = tl.maximum(1.0e-7, r2)
    th2 = TWO_PI * r3
    r2v = tl.sqrt(-2.0 * tl.math.log2(u2) * LN2)
    s2, c2 = fast_sin_cos(th2)
    n2 = r2v * c2
    n3 = r2v * s2

    # log-normal = exp(n*std + mean) = exp2(n*std*LOG2E + mean*LOG2E)
    sl = std * LOG2E
    ml = mean * LOG2E
    y0 = tl.math.exp2(tl.fma(n0, sl, ml))
    y1 = tl.math.exp2(tl.fma(n1, sl, ml))
    y2 = tl.math.exp2(tl.fma(n2, sl, ml))
    y3 = tl.math.exp2(tl.fma(n3, sl, ml))

    off_0 = tl.program_id(0) * BLOCK_SIZE * 4 + tl.arange(0, BLOCK_SIZE)
    off_1 = off_0 + BLOCK_SIZE
    off_2 = off_1 + BLOCK_SIZE
    off_3 = off_2 + BLOCK_SIZE

    tl.store(out_ptr + off_0, y0, mask=off_0 < N, eviction_policy="evict_first")
    tl.store(out_ptr + off_1, y1, mask=off_1 < N, eviction_policy="evict_first")
    tl.store(out_ptr + off_2, y2, mask=off_2 < N, eviction_policy="evict_first")
    tl.store(out_ptr + off_3, y3, mask=off_3 < N, eviction_policy="evict_first")


