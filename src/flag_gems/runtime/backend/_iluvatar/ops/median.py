# Fixed _f16_order_key
# -------------------------------------------------------------------


@triton.jit
def _f16_order_key(x):
    bits = x.to(tl.uint16, bitcast=True)
    signed = x.to(tl.int16, bitcast=True)
    sign = signed >> 15
    sign_mask = tl.full((), 0x8000, dtype=tl.uint16)
    # sign may be promoted to int32; use mask+cast instead of bitcast
    mask = sign_mask | (sign & 0xFFFF).to(tl.uint16)
    return bits ^ mask


