"""Iluvatar-specific median overrides.

Fixes two issues with the generic median implementation on the Iluvatar
backend's Triton version (3.1.x):

1. ``_f16_order_key``: the right-shift ``signed >> 15`` promotes int16
   to int32.  The subsequent ``sign.to(tl.uint16, bitcast=True)`` then
   fails because a 32-bit to 16-bit bitcast is invalid.  Fixed by
   masking and using a regular cast instead of a bitcast.

2. ``tl.sort`` does not support sub-32-bit integer types (int8, uint8,
   int16, bool).  The ``median_small_flat_kernel`` and
   ``median_small_dim_kernel`` are replaced with versions that accept a
   ``SORT_UPCAST`` constexpr to cast data to int32 before sorting and
   back afterward.
"""

