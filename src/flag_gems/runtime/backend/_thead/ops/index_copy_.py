@libentry()
@triton.jit
def index_copy_kernel(
    index,
    src,
    out,
    N,
    inp_numel,
    inp_stride_dim,
    inp_shape_dim,
    src_shape_dim,
    delta,
    src_stride0,
    src_stride1,
    src_stride2,
    src_shape0,
    src_shape1,
    src_shape2,
    rank: tl.constexpr,
    BLOCK_SIZE: tl.constexpr,
):
    pid = tl.program_id(axis=0)
    offsets = pid * BLOCK_SIZE + tl.arange(0, BLOCK_SIZE)
    mask = offsets < N

    # Decompose the flat src offset into per-axis src indices (rank <= 3).
    # off0 -> axis 0 (outermost), off1 -> axis 1, off2 -> axis 2 (innermost).
    if rank == 3:
        off0 = offsets // (src_shape1 * src_shape2)
        rem0 = offsets % (src_shape1 * src_shape2)
        off1 = rem0 // src_shape2
        off2 = rem0 % src_shape2
        src_offset = off0 * src_stride0 + off1 * src_stride1 + off2 * src_stride2
    elif rank == 2:
        off0 = offsets // src_shape1
        off1 = offsets % src_shape1
        src_offset = off0 * src_stride0 + off1 * src_stride1
    else:
        off0 = offsets
        src_offset = off0 * src_stride0

    pre_cal = inp_stride_dim * src_shape_dim
    pre_idx = (src_offset // pre_cal).to(tl.int64)
    dim_idx = (src_offset % pre_cal // inp_stride_dim).to(tl.int64)
    src_dim_idx = (tl.load(index + dim_idx, mask=mask, other=0)).to(tl.int64)
    input_idx = (
        src_offset + (delta * pre_idx + src_dim_idx - dim_idx) * inp_stride_dim
    ).to(tl.int64)

    input_mask = (input_idx >= 0) & (input_idx < inp_numel)
    store_mask = mask & input_mask
    src_val = tl.load(src + src_offset, mask=mask, other=0)
    tl.store(out + input_idx, src_val, mask=store_mask)


