def _fold_dim_to_last(input, dim):
    """Reshape ``input`` so the reduction axis is the last (contiguous,
    stride-1) axis and everything else flattens to the leading axis, returning
    the 2-D view plus the inverse reshape.

    For a contiguous tensor the reduction axis has stride 1 only when it is
    already the last axis; otherwise the elements of a sub-tensor are strided.
    To make the reduction axis stride-1 we permute it to the end and call
    ``.contiguous()`` (one copy) — the strided kernel then reads that contiguous
    buffer's last axis directly with no further permute/copy. The result is
    reshaped back to the original shape.
    """
    ndim = input.ndim
    perm = [i for i in range(ndim) if i != dim] + [dim]
    inv_perm = [0] * ndim
    for i, pi in enumerate(perm):
        inv_perm[pi] = i
    x_perm = input.permute(perm).contiguous()
    M = x_perm.numel() // x_perm.shape[-1]
    N = x_perm.shape[-1]
    # x_perm is (..., N) contiguous -> reshape to (M, N) is a view, N stride 1.
    x2d = x_perm.reshape(M, N)
    return x2d, x_perm, perm, inv_perm, M, N


