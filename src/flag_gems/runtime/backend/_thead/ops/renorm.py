def renorm_kernel(
    X,
    Y,
    M,
    N,
    stride_m,
    p,
    maxnorm,
    BLOCK_M: tl.constexpr,
    BLOCK_N: tl.constexpr,
):
    """Fused single-pass Lp renormalization, strided column-tile.

    Operates directly on a 2-D view of the tensor whose reduction axis is the
    last (contiguous, stride-1) axis: each program owns ``BLOCK_N`` sub-tensors
    (one per column) and reduces the ``M`` rows of each column in one tile.

    This fuses the norm computation and the scale-apply into a single launch
    (the generic layer uses two kernels and a redundant full-tensor pass) and,
    crucially, avoids the permute + contiguous copy the generic path performs
    to fold the reduction dim to the front: reading the strided columns directly
    removes one full-tensor read+write. fp32 accumulation for fp16/bf16 matches
    PyTorch's per-slice norm precision.

    ``X`` and ``Y`` may alias (in-place ``renorm_``): the norm is fully
    accumulated before any store, so the second (store) pass reading ``X`` again
    sees the original values.
    """
    pid_n = tl.program_id(0)
    offs_n = pid_n * BLOCK_N + tl.arange(0, BLOCK_N)
    offs_m = tl.arange(0, BLOCK_M)
    mask_n = offs_n < N
    mask_m = offs_m < M
    mask = mask_m[:, None] & mask_n[None, :]

    # fp32 accumulation for fp16/bf16 to match PyTorch's per-slice norm precision.
    if tl.constexpr(X.dtype.element_ty == tl.float16) or tl.constexpr(
        X.dtype.element_ty == tl.bfloat16
    ):
        cdtype = tl.float32
    else:
        cdtype = X.dtype.element_ty

    # Load the whole M-row column tile at once (M fits in BLOCK_M), accumulate
    # the per-column Lp-norm. The reduction axis is fully covered in one load,
    # so no inner loop is needed and correctness is independent of BLOCK_M.
    x = tl.load(
        X + offs_m[:, None] * stride_m + offs_n[None, :],
        mask=mask,
        other=0.0,
    ).to(cdtype)
    if p == 2.0:
        acc = tl.sum(x * x, axis=0)
        norm = tl_extra_shim.sqrt(acc)
    else:
        acc = tl.sum(tl_extra_shim.pow(tl.abs(x), p), axis=0)
        norm = tl_extra_shim.pow(acc, 1.0 / p)

    # scale = maxnorm / norm when norm > maxnorm, else 1.0 (no change).
    scale = tl.where(norm > maxnorm, maxnorm / norm, 1.0)

    y = x * scale[None, :]
    tl.store(
        Y + offs_m[:, None] * stride_m + offs_n[None, :],
        y.to(Y.dtype.element_ty),
        mask=mask,
    )


def _heuristics(M, N):
    """Pick the (BLOCK_M, BLOCK_N, num_warps) tile for thead PPU.

    ``BLOCK_M`` covers the full reduction length when it fits (single-tile
    load, no inner loop), capped at 1024 to bound register pressure. ``BLOCK_N``
    packs several sub-tensors (columns) per program so the fixed launch cost is
    amortized over more elements; it shrinks for large ``BLOCK_M`` to keep the
    BLOCK_M*BLOCK_N register tile bounded. ``num_warps`` follows the tile size.
    """
    BLOCK_M = min(triton.next_power_of_2(M), 1024)
    if BLOCK_M >= 1024:
        BLOCK_N = 2
        num_warps = 4
    elif BLOCK_M >= 256:
        BLOCK_N = min(triton.next_power_of_2(N), 8)
        num_warps = 4
    elif BLOCK_M >= 64:
        BLOCK_N = min(triton.next_power_of_2(N), 16)
        num_warps = 4 if BLOCK_N <= 4 else 8
    else:
        BLOCK_N = min(triton.next_power_of_2(N), 32)
        num_warps = 8
    return BLOCK_M, BLOCK_N, num_warps


