# Physicist's Hermite polynomial recurrence:
#   H_0(x) = 1, H_1(x) = 2*x,
#   H_{k+1}(x) = 2*x*H_k(x) - 2*k*H_{k-1}(x).
# Evaluated in float32 for numerical stability, matching the general layer.


@libentry()
@triton.jit
def hermite_polynomial_h_tensor_kernel(
    x_ptr,
    n_ptr,
    out_ptr,
    n_elements,
    BLOCK_SIZE: tl.constexpr,
):
    # Tensor-n path: every element carries its own degree, so all of H_2..H_9
    # must be produced and the right one selected per lane. The general layer
    # instead expands the closed form of every degree (each needs the full power
    # series x^2..x^9), which is ~6x more FLOPs than this 8-step recurrence. On
    # PPU that extra compute shows up on mid/tiny shapes; the huge shapes stay
    # memory-bound and break even.
    pid = tl.program_id(axis=0)
    offsets = pid * BLOCK_SIZE + tl.arange(0, BLOCK_SIZE)
    mask = offsets < n_elements

    x = tl.load(x_ptr + offsets, mask=mask, other=0.0)
    n = tl.load(n_ptr + offsets, mask=mask, other=0.0)
    n_int = n.to(tl.int32)

    xf = x.to(tl.float32)
    two_x = 2.0 * xf

    h_km1 = tl.full([BLOCK_SIZE], 1.0, tl.float32)  # H_0(x) = 1
    h_k = two_x  # H_1(x) = 2x

    result = tl.where(n_int == 0, h_km1, h_k)

    # Unroll the recurrence to degree 9: H_{k+1} = 2x*H_k - 2k*H_{k-1}.
    for k in tl.static_range(1, 9):
        h_kp1 = tl.fma(two_x, h_k, (-2.0 * k) * h_km1)
        h_km1 = h_k
        h_k = h_kp1
        result = tl.where(n_int == (k + 1), h_k, result)

    tl.store(out_ptr + offsets, result.to(x.dtype), mask=mask)


