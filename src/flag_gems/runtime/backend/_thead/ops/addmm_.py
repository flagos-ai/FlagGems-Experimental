def addmm_(self, mat1, mat2, *, beta=1, alpha=1):
    assert self.dtype.is_floating_point, "Only floating-point dtypes are supported"
    assert mat1.shape[1] == mat2.shape[0], "Incompatible dimensions"
    assert broadcastable_to(
        self.shape, (mat1.shape[0], mat2.shape[1])
    ), "Incompatible input shape"

    logger.debug("GEMS_THEAD ADDMM_")
    M, K = mat1.shape
    _, N = mat2.shape

    # In-place fused matmul + bias-add: launch the fused addmm kernel writing its
    # result directly into ``self`` instead of allocating a temporary output and
    # copying it back. On PPU, eager matmul is very fast and the generic
    # implementation (alloc + kernel + copy_, two extra launches) is dominated by
    # kernel-launch overhead, especially on small/medium shapes. Collapsing the
    # epilogue into a single launch that reads ``self`` as the bias and writes the
    # result back into the same tile removes the allocation and the trailing
    # copy. Aliasing bias (i_ptr) and output (c_ptr) on ``self`` is safe: every
    # program owns a disjoint output tile and reads its own bias tile before
    # writing it, so there is no cross-tile read-after-write hazard.
