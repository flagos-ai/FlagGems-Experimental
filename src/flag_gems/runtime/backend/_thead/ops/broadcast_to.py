import logging

import torch

logger = logging.getLogger(__name__)


def broadcast_to(x, size):
    """
    Broadcasts :attr:`input` to the shape :attr:`size`.
    Equivalent to calling ``input.expand(size)``.

    Args:
        x (Tensor): the input tensor.
        size (list, tuple, or torch.Size): the new shape.

    Returns:
        Tensor: The broadcasted tensor.
    """
    logger.debug("GEMS BROADCAST_TO")

    # broadcast_to is a pure view (meta) operation: torch eager resolves it with
    # near-zero overhead (~0.00016 ms) by merely adjusting shape/strides. Any
    # Triton kernel launch is strictly more expensive than that, so a data-copy
    # kernel always regresses on small/medium shapes (mean speedup ~0.27 in the
    # previous round). The only specialization that can avoid a regression is one
    # that performs no kernel launch at all and constructs a strided view instead.
    if not isinstance(size, (list, tuple, torch.Size)):
        raise TypeError("broadcast_to size must be a list/tuple/torch.Size of ints")

    size = list(size)
    in_shape = list(x.shape)
    in_ndim = len(in_shape)
    out_ndim = len(size)

    if in_ndim > out_ndim:
        raise RuntimeError(
            f"broadcast_to: requested size has fewer dimensions ({out_ndim}) "
            f"than input ({in_ndim})"
        )

    # Prepend singleton dims when the target has more dims than the input, so the
    # stride list lines up with ``size``. The inserted leading dims broadcast
    # (stride 0) unless their target size is also 1.
    if out_ndim > in_ndim:
        pad = out_ndim - in_ndim
        in_shape = [1] * pad + in_shape
        x = x.view(in_shape)

    in_strides = list(x.stride())

    # Resolve -1 and validate broadcastability, mirroring the general layer so
    # behaviour is preserved exactly.
    out_shape = []
    for d in range(out_ndim):
        req = size[d]
        src = in_shape[d]
        if req == -1:
            target = src
        else:
            target = req
        if src != target and src != 1:
            raise RuntimeError(
                f"The expanded size of the tensor ({target}) must match the existing size ({src}) "
                f"at non-singleton dimension {d}. Target sizes must be the same, or -1, "
                f"or the size of dimension in the original tensor must be 1."
            )
        out_shape.append(int(target))

    # Effective strides: 0 for broadcasted (singleton) dims, the source stride
    # otherwise. ``as_strided`` returns a view, so no data is copied and no
    # kernel is launched.
    strides = [in_strides[d] if in_shape[d] != 1 else 0 for d in range(out_ndim)]

    return torch.as_strided(x, out_shape, strides)
