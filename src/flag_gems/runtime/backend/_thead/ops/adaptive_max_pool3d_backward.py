import logging

import torch
import triton
import triton.language as tl

from flag_gems import runtime
from flag_gems.utils import libentry

logger = logging.getLogger(__name__)


@libentry()
@triton.autotune(
    configs=runtime.get_tuned_config("adaptive_max_pool3d_backward"),
    key=["n_elements"],
    reset_to_zero=["grad_input_ptr"],
)
@triton.jit
def adaptive_max_pool3d_backward_kernel(
    grad_output_ptr,
    indices_ptr,
    grad_input_ptr,
    # Shapes
    in_n,
    in_c,
    in_d,
    in_h,
    in_w,
    out_d,
    out_h,
    out_w,
    n_elements: tl.constexpr,
    BLOCK_SIZE: tl.constexpr,
):
    pid = tl.program_id(0)
    block_start = pid * BLOCK_SIZE
    offsets = block_start + tl.arange(0, BLOCK_SIZE)
    mask = offsets < n_elements

    # Compute (n, c, d_out, h_out, w_out) from flat offset
    # Total output elements per n,c is out_d * out_h * out_w
    out_per_nc = out_d * out_h * out_w

    # Flatten 5D position to 1D and then reconstruct
    nc_out = offsets // out_per_nc
    remainder = offsets % out_per_nc
    d_out = remainder // (out_h * out_w)
    remainder2 = remainder % (out_h * out_w)
    h_out = remainder2 // out_w
    w_out = remainder2 % out_w

    # n = nc_out // in_c, c = nc_out % in_c
    n_idx = nc_out // in_c
    c_idx = nc_out % in_c

    # Output is laid out as (n, c, d_out, h_out, w_out) with contiguous strides
    # for the last 3 dims.
    out_offset = (
        n_idx * in_c * out_per_nc
        + c_idx * out_per_nc
        + d_out * out_h * out_w
        + h_out * out_w
        + w_out
    )

    # Load the argmax index that was selected for this output position.
    idx = tl.load(indices_ptr + out_offset, mask=mask, other=0)

    # Convert the flat index back to a 3D position (D, H, W) in the input.
    spatial_size = in_h * in_w
    d_in = idx // spatial_size
    remainder = idx % spatial_size
    h_in = remainder // in_w
    w_in = remainder % in_w

    # Load the upstream gradient at the output position.
    grad_val = tl.load(grad_output_ptr + out_offset, mask=mask, other=0.0)

    # Compute the offset of the corresponding input gradient position.
    # Input is laid out as (n, c, d_in, h_in, w_in).
    in_offset = (
        n_idx * in_c * in_d * in_h * in_w
        + c_idx * in_d * in_h * in_w
        + d_in * in_h * in_w
        + h_in * in_w
        + w_in
    )

    # Atomically accumulate the gradient at the corresponding input position.
    # Atomic add is required because multiple output positions within the
    # adaptive pooling window can map to the same input position when input
    # spatial dims are not evenly divisible by the output size.
    #
    # The accumulation is performed directly in the input dtype instead of a
    # float32 staging buffer. For adaptive_max_pool3d the upstream gradient
    # always shares the input dtype, so accumulating in place avoids both the
    # doubled-memory float32 allocation and the final dtype-cast kernel, which
    # dominate latency on fp16/bf16 inputs. The trailing ``.to()`` in the host
    # wrapper therefore collapses to a no-op copy.
    tl.atomic_add(grad_input_ptr + in_offset, grad_val, mask=mask)


def adaptive_max_pool3d_backward(
    grad_output: torch.Tensor,
    self: torch.Tensor,
    indices: torch.Tensor,
):
    logger.debug("GEMS_THEAD ADAPTIVE_MAX_POOL3D_BACKWARD")

    in_n, in_c, in_d, in_h, in_w = self.shape
    out_d, out_h, out_w = (
        grad_output.shape[2],
        grad_output.shape[3],
        grad_output.shape[4],
    )

    # Accumulate directly in the input dtype. The gradient flows back to
    # ``self``, whose dtype always matches ``grad_output`` for this operator, so
    # no float32 staging buffer or final cast is needed.
    grad_input = torch.zeros_like(self)

    if grad_input.numel() == 0:
        return grad_input.to(grad_output.dtype)

    n_elements = in_n * in_c * out_d * out_h * out_w

    grid = lambda meta: (triton.cdiv(n_elements, meta["BLOCK_SIZE"]),)

    adaptive_max_pool3d_backward_kernel[grid](
        grad_output,
        indices,
        grad_input,
        in_n,
        in_c,
        in_d,
        in_h,
        in_w,
        out_d,
        out_h,
        out_w,
        n_elements,
    )

    return grad_input.to(grad_output.dtype)
