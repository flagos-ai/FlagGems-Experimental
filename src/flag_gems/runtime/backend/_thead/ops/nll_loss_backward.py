    mask_n = offsets_n < N

    tgt = tl.load(tgt_ptr + offsets_n, mask=mask_n, other=ignore_index)
    valid = (tgt != ignore_index) & mask_n

    if HAS_WEIGHT:
        wgt_tgt = tl.load(wgt_ptr + tgt, mask=valid, other=0).to(tl.float32)
    else:
        wgt_tgt = valid.to(tl.float32)

    if reduction == 0:
        out_grad = tl.load(out_grad_ptr + offsets_n, mask=mask_n, other=0).to(
            tl.float32
        )
    else:
        out_grad = tl.load(out_grad_ptr).to(tl.float32)
    if reduction == 1:
        total_w = tl.load(total_weight).to(tl.float32)
    else:
        total_w = 1.0

    inp_grad = tl.where(valid, -out_grad * wgt_tgt / total_w, 0.0)
    inp_grad_ptrs = inp_grad_ptr + offsets_n * C + tgt
    tl.store(inp_grad_ptrs, inp_grad, mask=mask_n)


