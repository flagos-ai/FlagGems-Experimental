# Below this output size (in elements) the launch-overhead saving from fusing
# the zero-fill and the scatter into a single kernel outweighs the per-element
# cost of the fused kernel's div/mod + broadcast compare. Above it the output
# buffer write becomes memory-bandwidth bound: a pure memset (which the generic
# layer's ``torch.zeros_like`` already runs at memcpy bandwidth) plus a tiny
# scatter is optimal, and the fused kernel's extra compute only contends with
# the store path and regresses. Measured crossover on thead PPU sits just above
# 1M elements (1024x1024 fused wins, 10000x256 fused loses), so small/medium
# shapes use the fused kernel and large shapes fall back to the memset + scatter
# path (matching the generic layer's latency).
_FUSED_THRESHOLD = 1 << 20  # 1,048,576 elements


