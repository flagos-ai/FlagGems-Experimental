import logging

import triton
import triton.language as tl

from flag_gems.runtime import torch_device_fn
from flag_gems.utils import libentry, tl_extra_shim

logger = logging.getLogger(__name__)

# When the per-slice reduction length N is at most this, a single tile covers the
# whole slice and the norm + in-place rescale fuse into one load/store pass.
# Larger N falls back to a two-pass (accumulate-then-rescale) loop in the same
# kernel launch.
_REDUCTION_FIT_CAP = 1024
# Target register tile size (elements) used to derive BLOCK_M from BLOCK_N.
_TILE_TARGET = 4096


