@triton.jit(do_not_specialize=["maxnorm"])
def renorm_2d_kernel(
    X,
    M,
    N,
    p_val,
    maxnorm,
    sM,
    sN,
    BLOCK_M: tl.constexpr,
    BLOCK_N: tl.constexpr,
    INPUT_FITS: tl.constexpr,
):
    """In-place renorm over a 2-D (M, N) strided view of the tensor.

    ``M`` is the size of the non-reduced axis (``dim``) and ``N`` is the
    reduction length (product of all other axes). One program owns ``BLOCK_M``
    consecutive slices; each slice's p-norm is computed over its ``N`` elements
    and, where it exceeds ``maxnorm``, the slice is rescaled in place.

    ``sM``/``sN`` are the strides (in elements) of the M and N axes, so the
    kernel runs directly on a permuted view without materializing a contiguous
    copy -- this removes the two full-tensor copies the generic in-place path
    performs (permute-contiguous + copy-back).
    """
    pid = tl.program_id(0).to(tl.int64)
    m_idx = pid * BLOCK_M + tl.arange(0, BLOCK_M)
    m_mask = m_idx < M
    m_idx = m_idx.to(tl.int64)

    if INPUT_FITS:
        # Single pass: the whole reduction fits in one tile, so load once,
        # compute the norm, rescale, and store -- no reload needed.
        n_idx = tl.arange(0, BLOCK_N)
        n_mask = n_idx < N
        offs = n_idx[:, None].to(tl.int64) * sN + m_idx[None, :] * sM
        mask = n_mask[:, None] & m_mask[None, :]
        x = tl.load(X + offs, mask=mask, other=0.0).to(tl.float32)
        a = tl.abs(x)
        if p_val == 2.0:
            norm = tl.sqrt(tl.sum(x * x, axis=0))
        elif p_val == 1.0:
            norm = tl.sum(a, axis=0)
        else:
            norm = tl_extra_shim.pow(
                tl.sum(tl_extra_shim.pow(a, p_val), axis=0), 1.0 / p_val
            )
        scale = tl.where(norm > maxnorm, maxnorm / norm, 1.0)
        # Round the per-slice scale to the input dtype before the multiply, so
        # fp16/bf16 outputs match PyTorch's per-slice scale rounding bit-for-bit.
        scale = scale.to(X.dtype.element_ty).to(tl.float32)
        y = (x * scale[None, :]).to(X.dtype.element_ty)
        tl.store(X + offs, y, mask=mask)
    else:
        # Two passes within one launch: accumulate the per-slice norm, then
        # reload and rescale. ``scale`` stays in registers across both passes.
        acc = tl.zeros([BLOCK_M], dtype=tl.float32)
        for n0 in range(0, N, BLOCK_N):
            n_idx = n0 + tl.arange(0, BLOCK_N)
            n_mask = n_idx < N
            offs = n_idx[:, None].to(tl.int64) * sN + m_idx[None, :] * sM
            mask = n_mask[:, None] & m_mask[None, :]
            x = tl.load(X + offs, mask=mask, other=0.0).to(tl.float32)
            a = tl.abs(x)
            if p_val == 2.0:
                acc += tl.sum(x * x, axis=0)
            elif p_val == 1.0:
                acc += tl.sum(a, axis=0)
            else:
                acc += tl.sum(tl_extra_shim.pow(a, p_val), axis=0)
        if p_val == 2.0:
            norm = tl.sqrt(acc)
        else:
            norm = tl_extra_shim.pow(acc, 1.0 / p_val)
        scale = tl.where(norm > maxnorm, maxnorm / norm, 1.0)
        scale = scale.to(X.dtype.element_ty).to(tl.float32)
        for n0 in range(0, N, BLOCK_N):
            n_idx = n0 + tl.arange(0, BLOCK_N)
            n_mask = n_idx < N
            offs = n_idx[:, None].to(tl.int64) * sN + m_idx[None, :] * sM
            mask = n_mask[:, None] & m_mask[None, :]
            x = tl.load(X + offs, mask=mask, other=0.0)
            y = (x.to(tl.float32) * scale[None, :]).to(X.dtype.element_ty)
            tl.store(X + offs, y, mask=mask)


def _block_heuristics(N):
    """Pick (BLOCK_M, BLOCK_N, INPUT_FITS, num_warps) for a reduction length N."""
    if N <= _REDUCTION_FIT_CAP:
        input_fits = True
        block_n = min(triton.next_power_of_2(N), _REDUCTION_FIT_CAP)
    else:
        input_fits = False
        block_n = 256
    block_m = max(8, min(256, _TILE_TARGET // block_n))
    tile = block_n * block_m
    if tile <= 1024:
        num_warps = 1
    elif tile <= 2048:
        num_warps = 2
    else:
        num_warps = 4
    return block_m, block_n, input_fits, num_warps


def _launch_2d(buf, M, N, sM, sN, p, maxnorm):
    block_m, block_n, input_fits, num_warps = _block_heuristics(N)
    grid = (triton.cdiv(M, block_m),)
