def renorm_(x, p, dim, maxnorm):
    logger.debug("GEMS_THEAD RENORM_")
    p = float(p)
    maxnorm = float(maxnorm)
    dim = dim % x.ndim

    if x.ndim == 2:
        # Fast path: operate in place on the original (possibly transposed)
        # strided view -- no permute-contiguous copy, no copy-back, one launch.
        other = 1 - dim
        M = x.shape[dim]
        N = x.shape[other]
        sM = x.stride(dim)
        sN = x.stride(other)
        _launch_2d(x, M, N, sM, sN, p, maxnorm)
        return x

    # General path for rank > 2: bring `dim` to the front, materialize a
    # contiguous (M, N) buffer, rescale in place, then copy back. These shapes
    # are small in practice so the extra copies are not on the hot path.
    perm = [dim] + [i for i in range(x.ndim) if i != dim]
    inv_perm = [perm.index(i) for i in range(x.ndim)]
    x_perm = x.permute(perm).contiguous()
    M = x_perm.shape[0]
    N = x_perm[0].numel()
    _launch_2d(x_perm, M, N, N, 1, p, maxnorm)
    x.copy_(x_perm.permute(inv_perm))
    return x
