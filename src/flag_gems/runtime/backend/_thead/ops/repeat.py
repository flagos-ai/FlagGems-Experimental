@libentry()
@triton.jit
def _repeat_axis0_kernel(
    in_ptr,
    out_ptr,
    n,
    K: tl.constexpr,
    BLOCK: tl.constexpr,
):
    """Fast path: repeat *only* along the outermost (axis-0) dimension.

    For a contiguous input the flattened output satisfies
    ``out_flat[idx] = in_flat[idx % n]`` (axis-0 repetition is just K
    concatenations of the whole flattened input), so each input element is read
    exactly once and written ``K`` times. This halves the read traffic of a
    memcpy-style ``cat`` (which reads the input ``K`` times) and avoids the
    per-element modular index reconstruction the generic kernel performs, which
    is the dominant win on the bandwidth-bound huge shapes.
    """
    pid = tl.program_id(0)
    offs = (pid * BLOCK + tl.arange(0, BLOCK)).to(tl.int64)
    mask = offs < n
    v = tl.load(in_ptr + offs, mask=mask)
    for k in tl.static_range(K):
        tl.store(out_ptr + k * n + offs, v, mask=mask)


@libentry()
@triton.jit
def _repeat_general_kernel(
    in_ptr,
    out_ptr,
    out_numel,
    OUT_STRIDES: tl.constexpr,
    IN_STRIDES: tl.constexpr,
    IN_SHAPES: tl.constexpr,
    RANK: tl.constexpr,
    BLOCK: tl.constexpr,
):
    """General path: arbitrary repeat pattern / non-contiguous input.

    Decomposes each flat output index into its row-major multi-index by walking
    the (constexpr) output strides from the outermost axis inward, takes it
    modulo the input shape per axis, and gathers from the (possibly strided)
    input. ``out[j] = input[j mod in_shape per axis]`` is exactly ``repeat``'s
    semantics, so this is correct for every shape; used as the fallback when the
    axis-0 fast path does not apply.

    The axis walk is a ``tl.static_range(RANK)`` loop over constexpr tuples, so
    a single kernel signature handles ANY rank without padding or a rank cap —
    each distinct rank simply compiles its own specialization. (Replaces the
    earlier fixed-slot ``s0..s5`` / ``os0..os5`` design, which capped rank at 6
    and missed the o5/s5/d5/os5 slots entirely.)
    """
    pid = tl.program_id(0)
    offs = (pid * BLOCK + tl.arange(0, BLOCK)).to(tl.int64)
    mask = offs < out_numel

    rem = offs
    in_off = tl.zeros_like(offs)
    for i in tl.static_range(RANK):
        o_i = rem // OUT_STRIDES[i]
        rem = rem % OUT_STRIDES[i]
        in_off += (o_i % IN_SHAPES[i]) * IN_STRIDES[i]

    v = tl.load(in_ptr + in_off, mask=mask)
    tl.store(out_ptr + offs, v, mask=mask)


def _align_ranks(x, sizes):
    """Pad ``sizes``/``x.shape`` with leading 1s so they share a common rank.

    Mirrors the rank alignment the generic layer performs before constructing the
    output shape, so the specialization preserves repeat semantics exactly:
    ``sizes`` is right-aligned and leading 1s are prepended to the input shape
    when ``len(sizes) > inp.ndim``.
    """
    d = list(sizes)
    sh = list(x.shape)
    r = max(len(sh), len(d))
    if len(d) < r:
        d = [1] * (r - len(d)) + d
    if len(sh) < r:
        sh = [1] * (r - len(sh)) + sh
        x = x.reshape(sh)
    return x, d, sh, r


def _is_axis0_single_repeat(d, r):
    """True when only the outermost dim is repeated (``d[0] >= 1`` and ``d[1:]`` all 1)."""
    if d[0] < 1:
        return False
    return all(d[i] == 1 for i in range(1, r))


