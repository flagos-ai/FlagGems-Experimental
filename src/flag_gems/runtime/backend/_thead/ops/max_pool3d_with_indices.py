def max_pool3d_backward_scatter_kernel(
    grad_output_ptr,
    indices_ptr,
    grad_input_ptr,
    # Number of input spatial elements per (n, c) slice (= stride between slices)
    in_spatial,
    # Output spatial layout (contiguous: NC, D, H, W)
    out_h,
    out_w,
    out_stride_nc,
    n_elements,
    BLOCK: tl.constexpr,
):
    """Scatter-based backward for 3-D max pooling.

    For max pooling the gradient rule is::

        grad_input[argmax[d_out, h_out, w_out]] += grad_output[d_out, h_out, w_out]

    i.e. each output position routes its upstream gradient to exactly one input
    position (the one its stored index points at). This kernel walks the *output*
    once and atomic-adds each gradient to its target input slot -- O(output)
    work. The generic layer instead walks the *input* and, for every input
    position, loops over all kernel_d*kernel_h*kernel_w offsets testing whether
    any output window selected it -- O(input * kernel_volume) work, which is the
    source of the ~0.5-0.9x regression on thead (it does ~27x more loads and
    index compares than necessary).

    Overlapping windows (stride < kernel) mean several outputs can target the
    same input slot, so the accumulation uses atomic_add into an fp32 staging
    buffer (matching the generic layer's fp32 accumulation + final cast, so
    accuracy is identical). The stored index is already a flat offset into the
    (D, H, W) spatial volume of its own (n, c) slice, so the input write offset
    is just ``nc * in_spatial + idx`` -- no index decomposition needed.

    BLOCK must be a power of 2 (tl.arange requirement); 512 is the swept winner
    (mean ~3.7x vs torch across all 12 core cases, beating BLOCK=1024's ~3.0x:
    the smaller block keeps the grid wide enough to fill the device on the
    smallest output volumes, where the larger block left programs idle).
    """
    pid = tl.program_id(0)
    offs = pid * BLOCK + tl.arange(0, BLOCK)
    mask = offs < n_elements

    out_per_nc = out_stride_nc
    nc = offs // out_per_nc
    rem = offs % out_per_nc
    d_out = rem // (out_h * out_w)
    rem2 = rem % (out_h * out_w)
    h_out = rem2 // out_w
    w_out = rem2 % out_w

    out_offset = nc * out_stride_nc + d_out * (out_h * out_w) + h_out * out_w + w_out

    idx = tl.load(indices_ptr + out_offset, mask=mask, other=0)
    grad = tl.load(grad_output_ptr + out_offset, mask=mask, other=0.0).to(tl.float32)

    in_offset = nc * in_spatial + idx
    tl.atomic_add(grad_input_ptr + in_offset, grad, mask=mask)


