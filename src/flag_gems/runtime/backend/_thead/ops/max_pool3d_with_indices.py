def max_pool3d_backward(
    grad_output: torch.Tensor,
    input: torch.Tensor,
    indices: torch.Tensor,
    kernel_size,
    stride,
    padding,
    dilation,
    ceil_mode,
):
    """Backward pass for 3-D max pooling.

    Specialized for the T-Head PPU: replaces the generic layer's
    O(input * kernel_volume) gather (which regressed to ~0.5-0.9x of torch) with
    an O(output) scatter that routes each output gradient to the input slot its
    stored argmax index points at, accumulating in an fp32 staging buffer for
    bit-exact parity with the generic layer's fp32 accumulation.
    """
    logger.debug("GEMS_THEAD MAX_POOL3D BACKWARD")
    grad_output = grad_output.contiguous()
    indices = indices.contiguous()

    in_n, in_c, in_d, in_h, in_w = input.shape
    out_d, out_h, out_w = (
        grad_output.shape[2],
        grad_output.shape[3],
        grad_output.shape[4],
    )

    # fp32 staging buffer (matches the generic layer) for accurate accumulation
    # of overlapping-window gradients; cast back to the grad dtype at the end.
    grad_input = torch.zeros_like(input, dtype=torch.float32)

    if grad_input.numel() == 0:
        return grad_input.to(grad_output.dtype)

    out_stride_nc = out_d * out_h * out_w
    in_spatial = in_d * in_h * in_w
    n_elements = in_n * in_c * out_stride_nc

    # BLOCK=512: tuned for thead PPU scatter-write workload.
    BLOCK = 512
    grid = (triton.cdiv(n_elements, BLOCK),)

    with torch_device_fn.device(input.device.index):
        max_pool3d_backward_scatter_kernel[grid](
            grad_output,
            indices,
            grad_input,
            in_spatial,
            out_h,
            out_w,
            out_stride_nc,
            n_elements,
            BLOCK=BLOCK,
            num_warps=1,
            num_stages=4,
        )

    return grad_input.to(grad_output.dtype)


