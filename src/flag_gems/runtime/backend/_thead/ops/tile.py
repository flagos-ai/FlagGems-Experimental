def tile(inp: torch.Tensor, dims) -> torch.Tensor:
    """Construct a tensor by repeating ``inp`` along each dimension per ``dims``.

    Specialized for the T-Head PPU:
      * An empty-result fast path avoids any kernel launch when any repeat is 0.
      * The common "repeat only the outermost axis on a contiguous tensor" case
        (every shape in the core benchmark) takes a single-read / K-write kernel
        that halves the read traffic of a memcpy-based repeat.
      * All other cases (multi-axis repeats, non-contiguous inputs) fall back to
        a general multi-index gather kernel that is correct for any rank.
    """
    logger.debug("GEMS_THEAD TILE")

    x, d, sh, r = _align_ranks(inp, dims)
    out_shape = [sh[i] * d[i] for i in range(r)]

    # Empty result: no data to copy, skip the kernel launch entirely.
    if any(s == 0 for s in out_shape):
        return torch.empty(out_shape, device=x.device, dtype=x.dtype)

    # Fast path: repeat only axis-0 on a contiguous tensor -> out_flat = in_flat
    # cyclically, so each input element is read once and written K times.
    if (
        r <= _MAX_RANK
        and d[0] >= 1
        and d[0] <= 8  # constexpr K: bound recompilation for huge repeat factors
        and _is_axis0_single_repeat(d, r)
        and x.is_contiguous()
    ):
        return _launch_axis0(x, d[0], out_shape)

    # General fallback: per-axis modulo gather, handles arbitrary rank/dims and
    # non-contiguous (strided) inputs.
    if r <= _MAX_RANK:
        return _launch_general(x, d, sh, r, out_shape)

    # Ranks beyond _MAX_RANK are not expected on thead. Reject explicitly rather
    # than recursing via torch.tile (which would dispatch back into this thead
    # specialization and loop infinitely). The general kernel is padded to
    # _MAX_RANK=6, which covers all realistic tile use cases.
    assert (
        r <= _MAX_RANK
    ), f"thead tile only supports output rank <= {_MAX_RANK}, got {r}"
