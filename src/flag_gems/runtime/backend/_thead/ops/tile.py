def _align_ranks(x, dims):
    """Pad ``dims``/``x.shape`` with leading 1s so they share a common rank.

    Mirrors the rank alignment the generic layer performs before constructing the
    output shape, so the specialization preserves tile semantics exactly.
    """
    d = list(dims)
    sh = list(x.shape)
    r = max(len(sh), len(d))
    if len(d) < r:
        d = [1] * (r - len(d)) + d
    if len(sh) < r:
        sh = [1] * (r - len(sh)) + sh
        x = x.reshape(sh)
    return x, d, sh, r


def _is_axis0_single_repeat(d, r):
    """True when only the outermost dim is repeated (``d[0] >= 1`` and ``d[1:]`` all 1)."""
    if d[0] < 1:
        return False
    return all(d[i] == 1 for i in range(1, r))


