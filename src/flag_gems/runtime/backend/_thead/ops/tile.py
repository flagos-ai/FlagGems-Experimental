# Upper rank supported by the general (multi-index) kernel. Tiled tensor shapes
# in practice never exceed 5 dimensions, and a constexpr rank keeps the index
# decomposition branch-free per specialization.
_MAX_RANK = 6


@libentry()
@triton.jit
def _tile_repeat_axis0_kernel(
    in_ptr,
    out_ptr,
    n,
    K: tl.constexpr,
    BLOCK: tl.constexpr,
):
    """Fast path: repeat *only* along the outermost (axis-0) dimension.

    For a contiguous input the output satisfies ``out_flat[idx] = in_flat[idx % n]``,
    so each input element is read exactly once and written ``K`` times. This
    halves the read traffic of a memcpy-style ``cat`` (which reads the input
    ``K`` times), which is the dominant win on the bandwidth-bound huge shapes.
    """
    pid = tl.program_id(0)
    offs = (pid * BLOCK + tl.arange(0, BLOCK)).to(tl.int64)
    mask = offs < n
    v = tl.load(in_ptr + offs, mask=mask)
    for k in tl.static_range(K):
        tl.store(out_ptr + k * n + offs, v, mask=mask)


