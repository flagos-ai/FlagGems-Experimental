logger = logging.getLogger(__name__)

# Small tensors (<= this many elements) are handled by a single-block kernel: it fuses
# the (!= 0) test, the prefix-sum and the index scatter into one launch with exactly one
# host sync (the .item() that reads back the nonzero count). The generic path instead
# issues three separate kernels (elementwise !=0, full-tensor cumsum, scatter) whose
# combined launch + dispatch overhead dominates compute on small shapes, where it
# regressed to ~0.3x of torch's native select. The block-local tl.cumsum is only cheap up
# to ~8192 lanes on this hardware (it grows super-linearly past that), so this is also the
# largest tile the single-block kernel can safely cover with a single program.
SINGLE_BLOCK_THRESHOLD = 8192

# Large-tensor pass uses this block size for the count + fill kernels. Swept across
# {1024,2048,4096,8192} x {4,8,16} warps: 2048 is the sweet spot — 8192 regresses (cumsum
# cost grows super-linearly with tile width), 1024 leaves the grid too wide and
# under-utilises the device on the bandwidth-bound fill pass.
TWO_PASS_BLOCK = 2048

# Inputs are flattened and treated as up to 4-D. Per-axis sizes are passed as plain scalar
# arguments (s0..s3, innermost axis first) rather than as a device tensor: building a
# torch.tensor(shape) costs ~0.03 ms of host + H2D overhead per call, which on the small
# shapes that dominate the launch-overhead regime would by itself erase any kernel win.
MAX_NDIM = 4


