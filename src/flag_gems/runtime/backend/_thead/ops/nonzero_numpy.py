def nonzero_fill_kernel(
    inp,
    block_counts,
    offsets,
    out,
    n_elements,
    s0,
    s1,
    s2,
    s3,
    N,
    ndim: tl.constexpr,
    BLOCK_SIZE: tl.constexpr,
):
    """Second pass: scatter indices at each block's exclusive-prefix offset.

    ``offsets`` holds the *inclusive* prefix sum of per-block counts, so a block's
    exclusive base is ``offsets[pid] - block_counts[pid]``. Deriving the base this way
    (rather than with a block-local atomic counter) keeps the output in stable input
    order: an atomic counter would race blocks for the global counter and scramble the
    ordering, failing torch's stable nonzero semantics.
    """
    pid = tl.program_id(0)
    off = pid * BLOCK_SIZE + tl.arange(0, BLOCK_SIZE)
    mask = off < n_elements
    vals = tl.load(inp + off, mask=mask, other=0)
    nz = (vals != 0) & mask

    cnt = tl.load(block_counts + pid)
    incl = tl.load(offsets + pid)
    base = incl - cnt  # exclusive prefix sum

    local_pos = tl.cumsum(nz.to(tl.int32))
    local_pos = tl.where(nz, local_pos - 1, 0)
    pos = base + local_pos

    idx_flat = off
    if ndim >= 4:
        d = idx_flat % s3
        idx_flat //= s3
        tl.store(out + 3 * N + pos, d.to(tl.int64), mask=nz)
    if ndim >= 3:
        d = idx_flat % s2
        idx_flat //= s2
        tl.store(out + 2 * N + pos, d.to(tl.int64), mask=nz)
    if ndim >= 2:
        d = idx_flat % s1
        idx_flat //= s1
        tl.store(out + 1 * N + pos, d.to(tl.int64), mask=nz)
    if ndim >= 1:
        d = idx_flat % s0
        tl.store(out + 0 * N + pos, d.to(tl.int64), mask=nz)


def _shape_scalars(inp):
    """Per-axis sizes as scalar ints, innermost axis first, padded to MAX_NDIM."""
    shape = list(inp.shape)
    if len(shape) > MAX_NDIM:
        raise ValueError(f"nonzero_numpy: input ndim {len(shape)} exceeds {MAX_NDIM}")
    shape = shape + [1] * (MAX_NDIM - len(shape))
    return shape


def _nonzero_single(inp, n_elements, ndim, shape):
    """Single-block path for small tensors (1 launch + 1 sync)."""
    flat = inp.view(n_elements)
    block = triton.next_power_of_2(max(n_elements, 4))
    num_warps = 4 if block <= 4096 else 8
    # Over-allocate one slot per element; trim to the real count after the sync.
    out = torch.empty(ndim, n_elements, dtype=torch.int64, device=inp.device)
    count = torch.empty(1, dtype=torch.int32, device=inp.device)
    with torch_device_fn.device(inp.device):
        nonzero_single_kernel[(1,)](
            flat,
            out,
            count,
            n_elements,
            shape[0],
            shape[1],
            shape[2],
            shape[3],
            ndim,
            BLOCK=block,
            num_warps=num_warps,
        )
    nnz = int(count.item())
    return [out[d, :nnz] for d in range(ndim)]


def _nonzero_two_pass(inp, n_elements, ndim, shape):
    """Two-pass count + fill path for large tensors (avoids full-tensor cumsum)."""
    flat = inp.view(n_elements)
    block = TWO_PASS_BLOCK
    grid = triton.cdiv(n_elements, block)
    block_counts = torch.empty(grid, dtype=torch.int32, device=inp.device)
    with torch_device_fn.device(inp.device):
        nonzero_count_kernel[(grid,)](flat, block_counts, n_elements, BLOCK=block)
    # Inclusive prefix sum of per-block counts. This is the only host sync: the last
    # element gives the total nonzero count, so we read nnz from it and reuse the same
    # tensor as the fill-pass offset table — one cumsum, one .item(), no extra allocation.
    offsets = block_counts.cumsum(axis=0)
    nnz = int(offsets[grid - 1].item())
    if nnz == 0:
        return [inp.new_empty(0, dtype=torch.int64) for _ in range(ndim)]
    out = torch.empty(ndim, nnz, dtype=torch.int64, device=inp.device)
    with torch_device_fn.device(inp.device):
        nonzero_fill_kernel[(grid,)](
            flat,
            block_counts,
            offsets,
            out,
            n_elements,
            shape[0],
            shape[1],
            shape[2],
            shape[3],
            nnz,
            ndim,
            BLOCK_SIZE=block,
            num_warps=8,
        )
    return [out[d] for d in range(ndim)]


def nonzero_numpy(inp):
    """
    Returns a tuple of 1D tensors, one for each dimension of the input,
    containing the indices of the non-zero elements in that dimension.

    This is equivalent to torch.nonzero(...).T or numpy.nonzero() behavior.
    """
    logger.debug("GEMS_THEAD NONZERO_NUMPY")

    inp = inp.contiguous()
    n_elements = inp.numel()
    ndim = inp.ndim

    if n_elements == 0:
        return [inp.new_empty(0, dtype=torch.int64) for _ in range(ndim)]

    shape = _shape_scalars(inp)

    # Two-tier strategy:
    #  * small tensor  -> single-block kernel (fused !=0 + prefix-sum + scatter)
    #  * large tensor  -> two-pass count + cumsum + fill (skips the full-tensor scan
    #                     that is the generic path's bottleneck on big inputs)
    # Both tiers pass the per-axis sizes as scalar args (no torch.tensor allocation) and
    # write a structure-of-arrays [ndim, N] int64 output, sliced to the nonzero count.
    if n_elements <= SINGLE_BLOCK_THRESHOLD:
        return _nonzero_single(inp, n_elements, ndim, shape)
    return _nonzero_two_pass(inp, n_elements, ndim, shape)
