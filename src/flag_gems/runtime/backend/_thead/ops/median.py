def _kept_names(names, dim, keepdim):
    if names is None:
        return None
    if keepdim:
        return names
    return names[:dim] + names[dim + 1 :]


def _use_radix(dtype, width):
    return (
        _supports_radix(dtype)
        and _RADIX_MIN_WIDTH <= width <= _RADIX_MAX_WIDTH
        and not dtype.is_complex
        and dtype != torch.bool
    )


def median(inp):
    logger.debug("GEMS_THEAD MEDIAN")

    inp = _anonymous(inp)
    if inp.numel() == 0:
        return _empty_result_value(inp)
    if inp.dtype.is_complex:
        raise RuntimeError("Sort does not support complex dtypes on CPU")
    if inp.numel() == 1:
        return inp.reshape(()).clone()

    n = inp.numel()
    if _use_radix(inp.dtype, n):
        return _radix_select_flat(inp.contiguous().reshape(-1))

    # Fallback to the generic layer for tiny / bool / complex dtypes where the
    # radix sweep is not amortised or not defined.
    from flag_gems.ops.median import median as _generic_median

    return _generic_median(inp)


