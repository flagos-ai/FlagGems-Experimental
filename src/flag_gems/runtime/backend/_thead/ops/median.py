MedianResult = namedtuple("median", ["values", "indices"])

# Radix-select replaces the generic layer's full ``tl.sort`` with a
# partial-selection sweep: median only needs the single middle element, but the
# generic path sorts every reduction (O(n log n)). The radix kernel processes
# ``nbits`` bits of the order-preserving key ``nbits/RADIX_BITS`` passes, each
# pass doing one histogram + prefix-sum over ``2**RADIX_BITS`` bins — O(n) per
# pass, independent of the value distribution. On thead PPU the per-launch cost
# of the sort dominates for the benchmark reduction widths (144..8192), and the
# radix sweep with a single launch beats it on every integer dtype and on
# fp16/bf16; for fp32 it matches or beats it on widths where sort's log factor
# is largest while falling back to the generic sort for the (rare) small-width
# fp32 cases where radix's fixed pass count is not amortised.
_RADIX_BITS = 8
_INT_RADIX_BITS = 4

# Reduction widths below this stay on the generic layer: the radix sweep's fixed
# ``nbits/RADIX_BITS`` passes are pure overhead when ``N`` is tiny, and the
# generic direct-sort / small-dim kernel is already optimal there.
_RADIX_MIN_WIDTH = 64

# Above this width the radix kernel's tile (``next_power_of_2(width)``) would
# exceed 8192 lanes, which trips the thead PPU compiler: the int32
# specialization (eight 4-bit passes over a 16384-wide tile) compiles for
# tens of minutes and the float specializations regress. The generic layer's
# binary-search key-select handles these large widths correctly and at
# comparable speed, so fall back to it past the boundary.
_RADIX_MAX_WIDTH = 8192


@triton.jit
def _order_key(vals, valid):
    """Map ``vals`` to a monotone unsigned key (NaN/invalid -> all-ones).

    Floats: flip the sign bit then (for negatives) invert all bits, giving the
    standard IEEE-754 total-order key. Signed ints: flip the sign bit. Unsigned
    ints: identity. Invalid lanes are forced to the all-ones key so they sort
    to the very end and never participate in the rank selection.
    """
    dtype = vals.dtype
    nbits: tl.constexpr = dtype.primitive_bitwidth
    utype = tl.dtype(f"uint{nbits}")
    full_mask: tl.constexpr = (1 << nbits) - 1
    if dtype.is_floating():
        bits = vals.to(utype, bitcast=True)
        top_mask: tl.constexpr = 1 << (nbits - 1)
        sign_mask = tl.where((bits & top_mask) != 0, full_mask, top_mask)
        key = bits ^ sign_mask
    elif dtype.is_int_signed():
        bits = vals.to(utype, bitcast=True)
        top_mask: tl.constexpr = 1 << (nbits - 1)
        key = bits ^ top_mask
    else:
        key = vals.to(utype)
    return tl.where(valid, key, tl.full(vals.shape, full_mask, dtype=utype))


@libentry()
@triton.jit
def median_radix_select_kernel(
    inp,
    out_values,
    out_indices,
    N,
    inner_size,
    BLOCK_N: tl.constexpr,
    RADIX_BITS: tl.constexpr,
    IS_FLOAT: tl.constexpr,
):
    """Per-output radix select of the median (rank ``(N-1)//2``).

    Each program owns one reduction sub-tensor laid out as ``N`` strided lanes
    (stride ``inner_size``), so the kernel works unchanged for both the
    last-dim contiguous case and the general case after the reduction axis is
    folded to the middle of a contiguous 3-D view. It computes the
    order-preserving key, then sweeps the key's bits high to low: each digit
    does a masked ``tl.histogram`` over ``2**RADIX_BITS`` bins, a prefix-sum to
    locate the bin containing the median rank, and narrows ``desired`` /
    ``rank`` for the next digit. After all digits, ``desired`` is the exact key
    of the median element and the first matching lane is its index.

    For floats, NaN must win: PyTorch's median returns NaN (and the index of
    the first NaN) whenever any NaN is present, regardless of rank, so we
    detect NaN up front and override the result. ``-inf`` / ``+inf`` flow
    through the radix path naturally because their order keys are the smallest
    / largest finite-encodable keys.
    """
    pid = tl.program_id(0)
    cols = tl.arange(0, BLOCK_N)
    valid = cols < N
    inner_off = pid % inner_size
    outer_off = pid // inner_size
    base = outer_off * N * inner_size + inner_off
    data = tl.load(inp + base + cols * inner_size, mask=valid, other=0)

    dtype = inp.dtype.element_ty
    nbits: tl.constexpr = dtype.primitive_bitwidth

    if IS_FLOAT:
        nan_mask = valid & (data != data)
        has_nan = tl.max(nan_mask.to(tl.int32), axis=0) != 0
        first_nan = tl.min(tl.where(nan_mask, cols, N), axis=0)
        nan_value = tl.load(inp + base + first_nan * inner_size, mask=has_nan, other=0)

    keys = _order_key(data, valid)
    kdt = keys.dtype

    radix_size: tl.constexpr = 1 << RADIX_BITS
    radix_bins = tl.arange(0, radix_size)
    radix_mask: tl.constexpr = radix_size - 1
    radix_mask_val = tl.full((), radix_mask, dtype=kdt)

    rank = (N - 1) // 2
    desired = tl.full((), 0, dtype=kdt)
    desired_mask = tl.full((), 0, dtype=kdt)

    for digit_pos in tl.static_range(nbits - RADIX_BITS, -1, -RADIX_BITS):
        active = valid & ((keys & desired_mask) == desired)
        digit = ((keys >> digit_pos) & radix_mask_val).to(tl.int32)
        counts = tl.histogram(digit, radix_size, active)
        cumsum = tl.cumsum(counts, axis=0)
        prev = cumsum - counts
        # The median rank falls in the first bin whose cumulative count exceeds
        # it; ties (prev == rank, i.e. the rank is exactly on a bin boundary)
        # resolve to the next bin so the chosen element is the first >= the
        # boundary, matching a stable lower-median selection.
        take = (cumsum > rank) & (prev <= rank)
        selected_bin = tl.min(tl.where(take, radix_bins, radix_size - 1), axis=0)
        counts_before = tl.max(tl.where(take, prev, 0), axis=0)
        selected_bin = selected_bin.to(kdt)
        desired = desired | (selected_bin << digit_pos)
        desired_mask = desired_mask | (radix_mask_val << digit_pos)
        rank = rank - counts_before

    key_match = valid & (keys == desired)
    first_match = tl.min(tl.where(key_match, cols, N), axis=0)
    selected_value = tl.load(inp + base + first_match * inner_size)

    if IS_FLOAT:
        selected_value = tl.where(has_nan, nan_value, selected_value)
        first_match = tl.where(has_nan, first_nan, first_match)

    tl.store(out_values + pid, selected_value)
    tl.store(out_indices + pid, first_match.to(tl.int64))


def _radix_bits_for(dtype):
    # 8-bit digits: fp16/bf16 collapse to a single pass, fp32 to four; int32 to
    # eight would be costly so ints use 4-bit digits (8 passes for int32, 4 for
    # int16) which the histogram handles in fewer registers and was faster in
    # sweeps on thead PPU.
    if dtype in (torch.int16, torch.int32, torch.int64, torch.int8, torch.uint8):
        return _INT_RADIX_BITS
    return _RADIX_BITS


def _launch_params(width, dtype):
    block_n = triton.next_power_of_2(width)
    if block_n <= 256:
        num_warps = 1
    elif block_n <= 1024:
        num_warps = 2
    else:
        num_warps = 4
    return block_n, num_warps


def _supports_radix(dtype):
    # Radix-select wins on the low-bit dtypes: fp16/bf16 collapse to two digit
    # passes and the integers use 4-bit digits, so the fixed pass count is small.
    # fp32 (four 8-bit passes) and fp64 were measured *slower* than the generic
    # layer's binary-search key-select on thead PPU, so they stay on the generic
    # path; int64 would need sixteen 4-bit passes and is also left to the
    # generic binary-search kernel. bool/complex are not radix-able.
    return dtype in (
        torch.float16,
        torch.bfloat16,
        torch.int8,
        torch.uint8,
        torch.int16,
        torch.int32,
    )


def _canonical_dim(ndim, dim):
    lower = -1 if ndim == 0 else -ndim
    upper = 0 if ndim == 0 else ndim - 1
    if dim < lower or dim > upper:
        raise IndexError(
            f"Dimension out of range (expected to be in range of "
            f"[{lower}, {upper}], but got {dim})"
        )
    return 0 if ndim == 0 else dim % ndim


