    block_start = pid * BLOCK_SIZE
    offsets = block_start + tl.arange(0, BLOCK_SIZE)
    mask = offsets < n_elements

    a = tl.load(a_ptr + offsets, mask=mask, other=0.0)
    x = tl.load(x_ptr + offsets, mask=mask, other=0.0)

    # Compute in float32 for better precision
    a_f32 = a.to(tl.float32)
    x_f32 = x.to(tl.float32)

    # Handle edge cases
    # P(a, 0) = 0 for a > 0; NaN for a <= 0 or x < 0
    result = tl.where((a_f32 > 0.0) & (x_f32 >= 0.0), 0.0, float("nan"))

    # Regularized lower incomplete gamma function P(a, x) for x > 0
    # Using series expansion for small x and continued fraction for large x.
    # Series expansion is better when x < a + 1.
    use_series = x_f32 < (a_f32 + 1.0)

    # Series expansion: P(a, x) = exp(-x) * x^a / Gamma(a) * sum_{n=0} x^n / (a)_n
    # with term recurrence term_0 = 1/a, term_n = term_{n-1} * x / (a + n).
    # Fixed iteration count (no data-dependent early exit — see module docstring).
    term = 1.0 / a_f32
    series_sum = term
    for i in range(1, SERIES_ITERS):
        term = term * x_f32 / (a_f32 + tl.cast(i, tl.float32))
        series_sum = series_sum + term

    # Divide by Gamma(a) to get the regularized value P(a, x)
    log_gamma_a = tl_extra_shim.lgamma(a_f32)
    series_result = tl.exp(-x_f32 + a_f32 * tl.log(x_f32) - log_gamma_a) * series_sum

    # Lentz's continued fraction for Q(a,x) = Gamma(a,x)/Gamma(a)
    # for the large-x regime (x >= a + 1).
    #
    # CF = b_0 + a_1/(b_1 + a_2/(b_2 + ...))
    #   b_0 = x + 1 - a
    #   a_n = n(a - n),  b_n = x + 2n + 1 - a   (n >= 1)
    # Then Q = e^{-x} * x^a / (Gamma(a) * CF) and P = 1 - Q.
    tiny = 1e-30
    b0 = x_f32 + 1.0 - a_f32
    f_val = b0
    C_val = b0
    # Initialize as a vector (matching x_f32's shape) so the loop-carried type
    # stays consistent across iterations; a scalar 0.0 initializer would be
    # widened to a vector on the first reassignment, which the frontend rejects.
    D_val = tl.zeros_like(x_f32)
    for i_val in range(1, CF_ITERS):
        i_f = tl.cast(i_val, tl.float32)
        an = i_f * (a_f32 - i_f)
        bn = x_f32 + 2.0 * i_f + 1.0 - a_f32

        D_val = bn + an * D_val
        D_val = tl.where(tl.abs(D_val) < tiny, tiny, D_val)

        C_val = bn + an / C_val
        C_val = tl.where(tl.abs(C_val) < tiny, tiny, C_val)

        D_val = 1.0 / D_val
        delta = C_val * D_val
        f_val = f_val * delta

    log_gamma_a = tl_extra_shim.lgamma(a_f32)
    log_q = a_f32 * tl.log(x_f32) - x_f32 - log_gamma_a - tl.log(f_val)
    q_val = tl.exp(log_q)
    # Clamp Q to [0, 1] via min/max (branch-free, PPU-friendly).
    q_val = tl.minimum(tl.maximum(q_val, 0.0), 1.0)
    frac_result = 1.0 - q_val

    # Combine results
    result = tl.where(
        (a_f32 > 0.0) & (x_f32 > 0.0),
        tl.where(use_series, series_result, frac_result),
        result,
    )

    # Store result
    tl.store(out_ptr + offsets, result, mask=mask)


