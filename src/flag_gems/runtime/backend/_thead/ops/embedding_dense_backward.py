@triton.jit
def _embedding_dense_backward_kernel(
    grad_output_ptr,
    indices_ptr,
    grad_weight_ptr,
    num_weights,
    padding_idx,
    N_ROWS,
    BLOCK_N: tl.constexpr,
    BLOCK_D: tl.constexpr,
    EMBED_DIM: tl.constexpr,
):
    # Each program scatters BLOCK_N rows of grad_output (each EMBED_DIM wide)
    # into grad_weight via atomic_add. The generic layer launches one program
    # per (row, D-tile) with a flat 1-D D-tile, which under-utilises the PPU:
    # it fixes BLOCK_D=128 / num_warps=4, and the tiny per-program work plus
    # the default 4-warp launch dominate the small/medium bench shapes. Tiling
    # rows x cols in 2-D lets a single program cover many more output rows per
    # launch (fewer programs, more work each) and pairs a wide D-tile with
    # num_warps=1, which profiling shows is the sweet spot on this hardware
    # (more warps oversubscribe the scatter/atomic path and regress).
    pid_n = tl.program_id(0)
    pid_d = tl.program_id(1)

    offs_n = pid_n * BLOCK_N + tl.arange(0, BLOCK_N)
    mask_n = offs_n < N_ROWS
    offs_d = pid_d * BLOCK_D + tl.arange(0, BLOCK_D)
    mask_d = offs_d < EMBED_DIM

    idx = tl.load(indices_ptr + offs_n, mask=mask_n, other=-1).to(tl.int32)
    valid = (idx != padding_idx) & (idx >= 0) & (idx < num_weights)

    # 2-D gather of the grad_output block: (BLOCK_N, BLOCK_D).
    offs = offs_n[:, None] * EMBED_DIM + offs_d[None, :]
    m = mask_n[:, None] & mask_d[None, :]
    go = tl.load(grad_output_ptr + offs, mask=m, other=0).to(tl.float32)

    # Scatter-add into the corresponding weight rows. Different rows may map to
    # the same weight row (duplicate indices), so accumulation must be atomic.
    gw_ptrs = idx[:, None] * EMBED_DIM + offs_d[None, :]
    tl.atomic_add(grad_weight_ptr + gw_ptrs, go, mask=(m & valid[:, None]))


