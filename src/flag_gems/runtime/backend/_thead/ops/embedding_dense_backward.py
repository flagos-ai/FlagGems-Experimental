def _block_heuristics(D):
    # D-tile: cover the whole embedding dim in one program when it is small
    # (avoids splitting a short row across programs), otherwise cap at 1024 to
    # bound register pressure. N-tile: ~2048 elements per program keeps enough
    # programs in flight for occupancy while amortising launch overhead, capped
    # at 16 rows so a single program never owns too many competing atomics.
    BLOCK_D = min(triton.next_power_of_2(D), 1024)
    BLOCK_N = max(1, min(2048 // BLOCK_D, 16))
    return BLOCK_N, BLOCK_D


